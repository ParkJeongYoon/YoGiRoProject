package kr.co.goodee39.vo;

public class ThemeCommentVO {
	int themecommentid;
	String themepostid;
	int themecode;
	String userid;
	String themecommentcontent;
	String themecommentdate;
	
	public int getThemecommentid() {
		return themecommentid;
	}
	public void setThemecommentid(int themecommentid) {
		this.themecommentid = themecommentid;
	}
	public String getThemepostid() {
		return themepostid;
	}
	public void setThemepostid(String themepostid) {
		this.themepostid = themepostid;
	}
	public int getThemecode() {
		return themecode;
	}
	public void setThemecode(int themecode) {
		this.themecode = themecode;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getThemecommentcontent() {
		return themecommentcontent;
	}
	public void setThemecommentcontent(String themecommentcontent) {
		this.themecommentcontent = themecommentcontent;
	}
	public String getThemecommentdate() {
		return themecommentdate;
	}
	public void setThemecommentdate(String themecommentdate) {
		this.themecommentdate = themecommentdate;
	}
}
